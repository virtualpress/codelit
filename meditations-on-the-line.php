      <div>
       <p>My second book of poems was rejected from my dream publisher. They said my work was too referential. What if I softly touched that rejection to see what it could teach me about longing? What if instead of pulling back on my references and allusions I went full maximalist? What if I dreamed bigger than my dream press? What is a dream press? Do you have a dream press or publication? If so, write it here: <input type="text" spellcheck="false">. This part of the poem is tactile.</p>
