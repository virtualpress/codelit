       <p>9.</p>
      </div>
      <div>
       <p>How do you encounter a line? The edge? The rim of a fingernail? A terminus? The end of yourself or a lover or a mother? How do you end your emails? I often write: <i>With gratitude, T.</i></p>
      </div>
