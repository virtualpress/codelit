      <div>
       <p>I am thankful for rejection. And what is rejection, if not another line break? A blue enjambment of water spilling off the table, which is the next line, the next project, next poem, next essay, next book, next nibble &mdash; all of it teaches you, all of it is still one poem, one massive volta, veering me to the next shift, if I'm pliable enough to let art pull me into all the different waves of my selves crashing into myself.</p>
